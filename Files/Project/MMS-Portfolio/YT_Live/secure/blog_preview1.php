            Dressed in red and white, children, sporting Santa caps, stand out from the rest of the crowd. The passers-by look at them curiously. The busy bankers on the other side of the counter peer over their spectacles as 11 of them, carrying their music notes, troop into the bank. The excitement is infectious as the children of Yellow Train Grade School halt in front of Axis Bank on Trichy Road to sing carols. Anouk Dubois, their French music teacher, hums and plays a tune on her Ukulele, gesturing the children to begin. And they do with gusto. They sing songs of the birth of Jesus Christ, the three kings and Mother Mary, and of warm fig-filled puddings. By the end of the performance, everyone in the bank is smiling and cheering ! And then the children bid them good-bye and climb into a van that trundles to the next destination.

Since Sunday, these children have been visiting hospitals, orphanages, homes of the differently-abled and a few private houses, to sing carols for them. As I follow them to a house at Sowripalayam on my two-wheeler, memories of Christmases in Kerala, come rushing back; of friends who brought home-baked Christmas cakes to school, the ritual of buying stars and my Christian nanny, who fed me plum cake. So many houses in my neighbourhood twinkled with stars, as kids would hope Santa would arrive before they went to bed.

Back to the present, the Yellow Train kids arrive at Menaka Manickaraj's home. She is a school teacher who is hosting the Christmas party. Menaka has arranged chairs outside and has a decorated and lit up tree. "This is so special for us. I have called over my sister and her family also to join in the fun," she says, as she hands out still-warm chocolate cup cakes to children.

Anouk says back home in France carol singing is a tradition. "Children go from house to house. The streets of Europe come alive with carols. Some parts of France hold a Christmas bazaar where people sell handmade jewellery, cakes and clothes."

The children once again gather together as they hear Anouk's call on Ukulele and the carol session begins. The parents also hum along. Anouk has taught them French, German and even Catalan songs.

