                                    <td>
                                    	<p style="color:#ca9963; font-size:24px; margin:0; text-align:center; padding:15px 0 2px 0; border-bottom:1.5px solid #ca9963;">The Director</p>
                                        <p style="line-height:23px; text-align:justify; margin:0; padding:5px 0;">Channakeshava is a Theatre director based in Bangalore.He has written and directed several plays for professionals,
                                         amateurs, children and for communities -- in many south Indian languages. He is visiting faculty for many institutions and universities. He visited Bread and Puppet 
                                         Theatre at Glover, Vermont, and was one of the Indian delegates for `Theatre Sutra' of British Council in Edinburgh, UK. He and his team Veeresh and Giridhar will be 
                                         leading the production.</p>
                                    </td>
                                  </tr>
                                  <tr>
                                    <td>
                                    	<p style="color:#ca9963; font-size:24px; margin:0; text-align:center; padding:15px 0 2px 0; border-bottom:1.5px solid #ca9963;">The Process</p>
                                        <p style="line-height:23px; text-align:justify; margin:0; padding:5px 0;">The approach that Channakeshava and his team will be bringing to theatre is totally refreshing. 
                                        There will be intense work on the body, voice, music, movement and the children will be engaged in everything from costume/stage design to music to the entire background 
                                        preparation of the play. There will be no use of plastic and 'cardboard' and 'papier mache' will be used to create the props etc. This brings a unique experience to the 
                                        children over the next two weeks.</p>
                                    </td>
                                  </tr>
