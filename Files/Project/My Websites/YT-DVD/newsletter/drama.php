                                    	<p style="line-height:23px; text-align:justify;">The last few weeks, with active sports everyday, have been high energy, energy that comes from competing, a different kind 
                                        of energy. We have had some valuable conversations with children around what it means to win with humility, what it means to lose with grace and what it means to play 
                                        with camaraderie and sportsmanship. From the spirit of sports, we now shift to a different mood - one of working and coming together for a theatrical production.</p>
