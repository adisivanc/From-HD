                                        <p style="line-height:23px; text-align:justify; margin:0; padding:5px 0;">Alibaba - Based on the play written by Jnanpeeth Awardee Dr. Chandrashekhara Kambar. The play is 
                                        a very interesting adaptation of 'Alibaba and Forty Thieves' of Arabian Nights. It is a musical farce written in South Indian Folk Style. It is abundant with humour and 
                                        music and is very engaging for the children.</p>
